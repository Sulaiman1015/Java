package Day11_ControlFlowStatement;

public class WhileLoops {

	public static void main(String[] args) {
		int i=1;
		while(i<=10) {
			System.out.println(i);
			i++;
		}
		
		int n=1;
		
		while(n<=10) {
			System.out.println("student "+n);
			n++;
		}
		int a=11;
		while(a>=1) {
			System.out.println(a);
			--a;
		}

	}

}

