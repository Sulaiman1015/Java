package Day5_Unary_Assignment_relation_operation;

public class AssignmentOperator {

	public static void main(String[] args) {
		
		int i=100;
		i+=1;
		System.out.println(i);
		
		i-=1;
		System.out.println(i);
		
		i*=2;
		System.out.println(i);
		
		i/=2;
		System.out.println(i);
		
		i%=3;
		System.out.println(i);
		

	}

}
